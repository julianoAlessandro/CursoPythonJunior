comprimento = int(input("Informe o comprimento da caixa em cm: "))
altura = int(input("Informe a altura  da caixa em cm : "))
largura = int(input("Informe a largura da caixa em cm : "))
Volume_caixa = comprimento * altura * largura
print(f'{comprimento} X {largura} x {altura} = {Volume_caixa}cm ^3')